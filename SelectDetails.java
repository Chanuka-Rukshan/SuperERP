    private void loadAssetDetails() {
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `asset_details`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("asset_details.id"));
                vector.add(resultSet.getString("asset_details.name"));
                vector.add(resultSet.getString("asset_details.details"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

