    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (evt.getClickCount() == 2) {

            String id = String.valueOf(jTable1.getValueAt(row, 0));
            String name = String.valueOf(jTable1.getValueAt(row, 1));
            String details = String.valueOf(jTable1.getValueAt(row, 2));

            assetSelect.setAssetDetailsId(id);
            assetSelect.setAssetDetailsName(name);
            assetSelect.setAssetDetailsDetails(details);

            this.dispose();
            // sr.getJTextField().grabFocus();

        }

        if (evt.getClickCount() == 1) {
            String name = String.valueOf(jTable1.getValueAt(row, 1));
            String details = String.valueOf(jTable1.getValueAt(row, 2));

            jTextField1.setText(name);
            jTextArea1.setText(details);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String details = jTextArea1.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Asset Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (name.length() > 100) {
            JOptionPane.showMessageDialog(this, "Asset Name Must Contain LOWER THAN 100 characters", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (details.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Asset Details", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `asset_details` WHERE `name` = '" + name + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Asset Name is Alrady Rejistered", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL.executeIUD("INSERT INTO `asset_details` (`name`,`details`) VALUES ('" + name + "','" + details + "')");
                    JOptionPane.showMessageDialog(this, "Success", "Information  ", JOptionPane.INFORMATION_MESSAGE);

                    reset();
                    loadAssetDetails();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();
        String details = jTextArea1.getText();

        int row = jTable1.getSelectedRow();
        String id = String.valueOf(jTable1.getValueAt(row, 0));

        System.out.println(id);

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Asset Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (name.length() > 100) {
            JOptionPane.showMessageDialog(this, "Asset Name Must Contain LOWER THAN 100 characters", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (details.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Asset Details", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `asset_details` WHERE `id` = '" + id + "'");

                if (resultSet.next()) {
                    MySQL.executeIUD("UPDATE `asset_details` SET `name` = '" + name + "' , `details` = '" + details + "' WHERE `id` = '" + id + "'");
                    JOptionPane.showMessageDialog(this, "Success", "Information  ", JOptionPane.INFORMATION_MESSAGE);

                    reset();
                    loadAssetDetails();
                }

            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

